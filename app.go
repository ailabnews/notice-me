// SetBlinkEnabled toggles the blink effect setting.
func (a *App) SetBlinkEnabled(enabled bool) error {
	next := a.cfg.Snapshot()
	next.Behavior.BlinkEnabled = enabled
	a.cfg.Apply(next)
	if !enabled {
		a.stopBlinking()
	}
	return a.cfg.Save()
}

// SetStopHookEnabled toggles whether Stop/StopFailure hook events are included.
func (a *App) SetStopHookEnabled(enabled bool) error {
	next := a.cfg.Snapshot()
	next.Behavior.StopHookEnabled = enabled
	a.cfg.Apply(next)
	return a.cfg.Save()
}

// GetHookStatus returns the current Claude Code hook configuration status.
func (a *App) GetHookStatus() string {
	status, err := claudeconfig.GetStatus()
	if err != nil {
		b, _ := json.Marshal(map[string]any{"error": err.Error()})
		return string(b)
	}
	b, _ := json.Marshal(status)
	return string(b)
}

// ConfigureHooks writes notify-me hook entries into Claude Code settings.
// Returns an error if Claude Code is not installed.
func (a *App) ConfigureHooks(mode string) error {
	status, err := claudeconfig.GetStatus()
	if err != nil {
		return fmt.Errorf("检测 Claude Code 状态失败: %w", err)
	}
	if !status.Installed {
		return fmt.Errorf("Claude Code 未安装，请先安装 Claude Code CLI")
	}
	snap := a.cfg.Snapshot()
	httpURL := fmt.Sprintf("http://%s:%d%s/claude/hook",
		snap.Server.Host, snap.Server.Port, snap.Server.EndpointPrefix)
	return claudeconfig.Configure(mode, httpURL, snap.Behavior.StopHookEnabled)
}

// RemoveHooks removes notify-me hook entries from Claude Code settings.
func (a *App) RemoveHooks() error {
	return claudeconfig.Remove()
}

// DashboardStats returns aggregated statistics for the home dashboard.
func (a *App) DashboardStats() string {
	if a.db == nil {
		return "{}"
	}
	ctx := a.ctx
	tools, _ := a.db.ToolUsageCount(ctx)
	decisions, _ := a.db.DecisionStats(ctx)
	avgMs, _ := a.db.AvgResponseTime(ctx)
	sessions, _ := a.db.SessionIDs(ctx)
	total, _ := a.db.TotalCount(ctx)
	b, _ := json.Marshal(map[string]any{
		"tool_usage":      tools,
		"decisions":       decisions,
		"avg_response_ms": avgMs,
		"sessions":        sessions,
		"total":           total,
	})
	return string(b)
}

// SessionTimeline returns recent records for a specific session.
func (a *App) SessionTimeline(sessionID string, limit int) string {
	if a.db == nil {
		return "[]"
	}
	if limit <= 0 {
		limit = 50
	}
	recs, err := a.db.RecentBySession(a.ctx, sessionID, limit)
	if err != nil {
		b, _ := json.Marshal(map[string]any{"error": err.Error()})
		return string(b)
	}
	b, _ := json.Marshal(recs)
	return string(b)
}

