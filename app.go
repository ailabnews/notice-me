// ===== Tray icon blink =====

// startBlinking begins alternating the tray icon between the normal and alert
// versions. If already blinking this is a no-op.
func (a *App) startBlinking() {
	a.blinkMu.Lock()
	defer a.blinkMu.Unlock()
	if a.blinkStop != nil {
		return // already blinking
	}
	stop := make(chan struct{})
	a.blinkStop = stop
	go a.blinkLoop(stop)
}

// stopBlinking stops the blink goroutine and restores the normal icon.
func (a *App) stopBlinking() {
	a.blinkMu.Lock()
	stop := a.blinkStop
	a.blinkStop = nil
	a.blinkMu.Unlock()
	if stop != nil {
		close(stop)
	}
	if a.tray != nil {
		a.tray.SetIcon(trayIconPNG)
	}
}

// blinkLoop runs on a background goroutine and pulses the tray icon in a
// heartbeat pattern: two quick flashes then a short pause, repeating until
// the stop channel is closed.
func (a *App) blinkLoop(stop <-chan struct{}) {
	// heartbeat pattern: flash-pause-flash-pause-longer-pause
	pattern := []time.Duration{
		250 * time.Millisecond, // show alert
		250 * time.Millisecond, // show normal
		250 * time.Millisecond, // show alert
		800 * time.Millisecond, // show normal (longer pause)
	}
	for {
		for i, d := range pattern {
			select {
			case <-stop:
				return
			case <-time.After(d):
				if i%2 == 0 {
					a.tray.SetIcon(a.alertIcon)
				} else {
					a.tray.SetIcon(trayIconPNG)
				}
			}
		}
	}
}

// onNotificationResolved is the dispatcher OnResolve callback. It stops the
// blink when all in-flight notifications have been resolved.
func (a *App) onNotificationResolved() {
	select {
	case <-a.ctx.Done():
		return
	default:
	}
	if a.disp.InFlightCount() == 0 {
		a.stopBlinking()
	}
}

// Shutdown unwinds the runtime subsystems in dependency order: server first
// (so no new dispatcher work arrives), then context cancel (which drains the
// dispatcher's in-flight notifications), then SQLite.
func (a *App) Shutdown() {
	a.stopBlinking()
	if a.win != nil {
		a.win.CloseAllDiff()
	}
	if a.server != nil {
		_ = a.server.Stop(context.Background())
	}
	_ = singleton.Release()
	if a.cancel != nil {
		a.cancel()
	}
	if a.db != nil {
		_ = a.db.Close()
	}
	for _, fn := range a.cleanup {
		fn()
	}
}

// ===== Policy rule management (Wails methods) =====

// GetPolicyRules returns all policy rules as a JSON string.
func (a *App) GetPolicyRules() string {
	if a.db == nil {
		return "[]"
	}
	rules, err := a.db.ListPolicyRules(a.ctx)
	if err != nil {
		b, _ := json.Marshal(map[string]any{"error": err.Error()})
		return string(b)
	}
	b, _ := json.Marshal(rules)
	return string(b)
}

// AddPolicyRule inserts a new policy rule and reloads the engine.
func (a *App) AddPolicyRule(jsonStr string) error {
	if a.db == nil {
		return fmt.Errorf("storage not initialized")
	}
	var r policy.PolicyRule
	if err := json.Unmarshal([]byte(jsonStr), &r); err != nil {
		return err
	}
	_, err := a.db.InsertPolicyRule(a.ctx, r)
	if err != nil {
		return err
	}
	a.policy.Reload()
	return nil
}

// UpdatePolicyRule updates an existing policy rule and reloads the engine.
func (a *App) UpdatePolicyRule(jsonStr string) error {
	if a.db == nil {
		return fmt.Errorf("storage not initialized")
	}
	var r policy.PolicyRule
	if err := json.Unmarshal([]byte(jsonStr), &r); err != nil {
		return err
	}
	if err := a.db.UpdatePolicyRule(a.ctx, r); err != nil {
		return err
	}
	a.policy.Reload()
	return nil
}

// DeletePolicyRule deletes a policy rule by ID and reloads the engine.
func (a *App) DeletePolicyRule(id int64) error {
	if a.db == nil {
		return fmt.Errorf("storage not initialized")
	}
	if err := a.db.DeletePolicyRule(a.ctx, id); err != nil {
		return err
	}
	a.policy.Reload()
	return nil
}

// CleanupSessionRules removes session-scoped rules for inactive sessions.
func (a *App) CleanupSessionRules() error {
	if a.db == nil {
		return fmt.Errorf("storage not initialized")
	}
	if err := a.db.CleanupInactiveSessionRules(a.ctx); err != nil {
		return err
	}
	a.policy.Reload()
	return nil
}

// policyStoreAdapter adapts *storage.Storage to the policy.PolicyStore
// interface by wrapping the context-less ListPolicyRules call.
type policyStoreAdapter struct {
	db *storage.Storage
}

func (a *policyStoreAdapter) ListPolicyRules() ([]policy.PolicyRule, error) {
	return a.db.ListPolicyRules(context.Background())
}

// makeAlertIcon generates an "alert" version of the tray icon by tinting the
// background to a vibrant red while keeping the "NM" text white. Falls back to
// the original if decoding fails.
func makeAlertIcon(original []byte) []byte {
	src, err := png.Decode(bytes.NewReader(original))
	if err != nil {
		return original
	}
	b := src.Bounds()
	dst := image.NewRGBA(b)
	draw.Draw(dst, b, src, b.Min, draw.Src)

	// Replace dark blue (#0d2b4e) background with alert red (#E8413C),
	// keep white text unchanged.
	for y := 0; y < b.Dy(); y++ {
		for x := 0; x < b.Dx(); x++ {
			r, g, b_, a := dst.At(x, y).RGBA()
			if a == 0 {
				continue
			}
			// White text (high brightness) stays white.
			if r>>8 > 200 && g>>8 > 200 && b_>>8 > 200 {
				continue
			}
			dst.Set(x, y, color.RGBA{R: 232, G: 65, B: 60, A: uint8(a >> 8)})
		}
	}

	var buf bytes.Buffer
	if err := png.Encode(&buf, dst); err != nil {
		return original
	}
	return buf.Bytes()
}
